import { test, describe, beforeEach } from 'node:test';
import assert from 'node:assert';
import { store } from './store.js';

// Simple localStorage mock for Node.js environment
global.localStorage = {
  store: {},
  getItem(key) {
    return this.store[key] || null;
  },
  setItem(key, value) {
    this.store[key] = String(value);
  },
  removeItem(key) {
    delete this.store[key];
  },
  clear() {
    this.store = {};
  }
};

describe('DataStore', () => {
  beforeEach(() => {
    // Clear mock localStorage and store listeners before each test
    global.localStorage.clear();
    store.listeners = {};
  });

  describe('getAll', () => {
    test('returns empty array when no data exists', () => {
      const result = store.getAll('users');
      assert.deepStrictEqual(result, []);
    });

    test('returns parsed data when it exists in localStorage', () => {
      const users = [{ id: '1', name: 'Alice' }];
      global.localStorage.setItem('simpro_users', JSON.stringify(users));

      const result = store.getAll('users');
      assert.deepStrictEqual(result, users);
    });

    test('returns empty array on parse failure', () => {
      global.localStorage.setItem('simpro_users', 'invalid json');

      const result = store.getAll('users');
      assert.deepStrictEqual(result, []);
    });
  });

  describe('save', () => {
    test('saves stringified data to localStorage', () => {
      const items = [{ id: '1', name: 'Bob' }];
      store.save('customers', items);

      const stored = global.localStorage.getItem('simpro_customers');
      assert.strictEqual(stored, JSON.stringify(items));
    });

    test('emits event on save', () => {
      const items = [{ id: '2', name: 'Charlie' }];
      let emittedData = null;
      let emitCount = 0;

      store.on('customers', (data) => {
        emittedData = data;
        emitCount++;
      });

      store.save('customers', items);

      assert.strictEqual(emitCount, 1);
      assert.deepStrictEqual(emittedData, items);
    });
  });

  describe('create', () => {
    test('creates item with generated id and timestamps', () => {
      const item = { name: 'David' };
      const created = store.create('employees', item);

      assert.ok(created.id);
      assert.ok(created.createdAt);
      assert.ok(created.updatedAt);
      assert.strictEqual(created.name, 'David');
      // createdAt and updatedAt are generated by two separate calls to `new Date().toISOString()`,
      // so we can't reliably assert they are identical down to the millisecond.
      // However, we can assert that both properties exist.

      // Check it was saved
      const all = store.getAll('employees');
      assert.strictEqual(all.length, 1);
      assert.deepStrictEqual(all[0], created);
    });

    test('preserves provided id and createdAt', () => {
      const item = {
        id: 'custom_id',
        name: 'Eve',
        createdAt: '2023-01-01T00:00:00.000Z'
      };
      const created = store.create('employees', item);

      assert.strictEqual(created.id, 'custom_id');
      assert.strictEqual(created.createdAt, '2023-01-01T00:00:00.000Z');
      assert.ok(created.updatedAt);
      // updatedAt should be new, so not equal to createdAt (unless tests are lightning fast, but ISO string guarantees difference or recent time)
    });
  });

  describe('update', () => {
    test('updates existing item and its updatedAt timestamp', () => {
      const item = store.create('jobs', { title: 'Fix roof' });
      const oldUpdatedAt = item.updatedAt;

      // Delay to ensure updatedAt is different
      const start = Date.now();
      while (Date.now() - start < 5) {}

      const updated = store.update('jobs', item.id, { title: 'Fix window', status: 'done' });

      assert.strictEqual(updated.title, 'Fix window');
      assert.strictEqual(updated.status, 'done');
      assert.strictEqual(updated.id, item.id);
      assert.notStrictEqual(updated.updatedAt, oldUpdatedAt);

      // Check it was saved
      const all = store.getAll('jobs');
      assert.strictEqual(all.length, 1);
      assert.deepStrictEqual(all[0], updated);
    });

    test('returns null when updating non-existent item', () => {
      const result = store.update('jobs', 'non-existent-id', { title: 'New' });
      assert.strictEqual(result, null);
    });
  });

  describe('getSettings', () => {
    test('returns default settings when no data exists', () => {
      const settings = store.getSettings();
      assert.strictEqual(settings.markupPercent, 20);
      assert.strictEqual(settings.laborRates.length, 3);
      assert.strictEqual(settings.laborRates[0].name, 'Standard Rate');
    });

    test('returns stored settings when they exist', () => {
      const customSettings = {
        markupPercent: 25,
        laborRates: [{ id: 'r1', name: 'Custom', rate: 100 }]
      };
      global.localStorage.setItem(store._key('settings'), JSON.stringify(customSettings));

      const result = store.getSettings();
      assert.deepStrictEqual(result, customSettings);
    });

    test('returns defaults on parse failure', () => {
      global.localStorage.setItem(store._key('settings'), 'invalid json');

      const result = store.getSettings();
      assert.strictEqual(result.markupPercent, 20);
      assert.strictEqual(result.laborRates.length, 3);
      assert.strictEqual(result.laborRates[0].name, 'Standard Rate');
    });
  });

  describe('saveSettings', () => {
    test('saves settings to localStorage', () => {
      const settings = { markupPercent: 30, laborRates: [] };
      store.saveSettings(settings);

      const stored = global.localStorage.getItem(store._key('settings'));
      assert.strictEqual(stored, JSON.stringify(settings));
    });

    test('emits event on saveSettings', () => {
      const settings = { markupPercent: 30, laborRates: [] };
      let emittedData = null;
      let emitCount = 0;

      store.on('settings', (data) => {
        emittedData = data;
        emitCount++;
      });

      store.saveSettings(settings);

      assert.strictEqual(emitCount, 1);
      assert.deepStrictEqual(emittedData, settings);
    });
  });
});
